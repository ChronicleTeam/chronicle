use crate::{model::Table, Id};
use sqlx::{Acquire, PgExecutor, Postgres};

pub async fn create_table(
    connection: impl Acquire<'_, Database = Postgres>,
    user_id: Id,
    name: String,
    description: String,
) -> sqlx::Result<Id> {
    let mut transaction = connection.begin().await?;

    let (table_id, data_table_name): (Id, String) = sqlx::query_as(
        r#"
            INSERT INTO meta_table (user_id, name, description)
            VALUES ($1, $2, $3) 
            RETURNING table_id, data_table_name
        "#,
    )
    .bind(user_id)
    .bind(name)
    .bind(description)
    .fetch_one(transaction.as_mut())
    .await?;

    // data_table_name generated by database NO INJECTION POSSIBLE

    sqlx::query(&format!(
        r#"
            CREATE TABLE {data_table_name} (
                created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
                updated_at TIMESTAMPTZ,
            )
        "#,
    ))
    .execute(transaction.as_mut())
    .await?;

    sqlx::query(&format!(
        r#"SELECT trigger_updated_at('{data_table_name}')"#
    ))
    .execute(transaction.as_mut())
    .await?;

    transaction.commit().await?;

    Ok(table_id)
}

pub async fn get_data_table_name(
    executor: impl PgExecutor<'_>,
    table_id: Id,
) -> sqlx::Result<String> {
    let (data_table_name,): (String,) = sqlx::query_as(
        r#"
            SELECT data_table_name
            FROM meta_table
            WHERE table_id = $1
        "#,
    )
    .bind(table_id)
    .fetch_one(executor)
    .await?;
    Ok(data_table_name)
}

pub async fn get_user_tables(
    executor: impl PgExecutor<'_>,
    user_id: Id,
) -> sqlx::Result<Vec<Table>> {
    Ok(sqlx::query_as(
        r#"
            SELECT
                table_id,
                user_id,
                name,
                description,
                created_at,
                updated_at
            FROM meta_table
            WHERE user_id = $1
        "#,
    )
    .bind(user_id)
    .fetch_all(executor)
    .await?)
}

pub async fn update_table(
    executor: impl PgExecutor<'_>,
    table_id: Id,
    name: String,
    description: String,
) -> sqlx::Result<()> {
    sqlx::query(
        r#"
            UPDATE meta_table
            SET name = $1, description = $2
            WHERE table_id = $3
        "#,
    )
    .bind(name)
    .bind(description)
    .bind(table_id)
    .execute(executor)
    .await?;

    Ok(())
}

pub async fn delete_table(
    connection: impl Acquire<'_, Database = Postgres>,
    table_id: Id,
) -> sqlx::Result<()> {
    let mut transaction = connection.begin().await?;

    let (data_table_name,): (String,) = sqlx::query_as(
        r#"
            DELETE FROM meta_table
            WHERE table_id = $1
            RETURNING data_table_name
        "#,
    )
    .bind(table_id)
    .fetch_one(transaction.as_mut())
    .await?;

    sqlx::query(&format!(r#"DROP TABLE {data_table_name}"#))
        .execute(transaction.as_mut())
        .await?;

    Ok(())
}
